"""
Heart Rate Variability (HRV) Analysis Pipeline
==============================================

**Description:**
    This script extracts HRV metrics from segmented ECG/Heart Rate data generated by `segmenting.py`.
    It follows a hierarchical structure (Subject > Session > Run) to compute Time-Domain and/or Frequency-Domain
    HRV metrics for specific experiment segments (Phases, Trials, Blocks).

**Key Features:**
    1.  **Metric Computation**: Uses `neurokit2` to calculate standard HRV metrics:
        -   Time-Domain: MeanRR, SDNN, RMSSD, pNN50, etc.
        -   Frequency-Domain: LF, HF, LF/HF Ratio.
    2.  **Flexible Segmentation**: Can analyze multiple segment types:
        -   **Phases**: Short durations around specific events (e.g., 'AgentStopped').
        -   **Trials**: Full trial durations.
        -   **Blocks**: Entire experimental blocks.
    3.  **Source Flexibility**: Supports RR intervals from two sources:
        -   `bbsig`: Derived from raw ECG signal processing (more accurate).
        -   `calc`: Derived from Polar band heart rate (consumer grade).

**Inputs:**
    -   Segmented Data JSON: `data/derivatives/segmenting/sub-XXX/ses-XXX/sub-XXX_..._segmented_data.json`.
    -   Content: Contains lists of RR intervals/peaks for each defined segment.

**Outputs:**
    -   HRV Analysis Results: `data/derivatives/hrv-analysis/sub-XXX/ses-XXX/*.json`.
    -   Files are split by segment type (e.g., `hrv-phases_bbsig.json`, `hrv-trials_calc.json`).

**Usage:**
    `python code/hrv_extraction.py`
    (Edit the 'CONFIGURATION' section at the top of the script to select subjects/metrics)

"""

