# ========== HELPER FUNCTIONS ==========

def convert_peaks_to_rr(rr_dict, rr_source='rr_bbsig'):
    """
    Convert RR intervals to format for HRV analysis.

    Parameters:
    - rr_dict (dict): Dictionary with 'rr_bbsig' or 'rr_calc' keys from segmented data
    - rr_source (str): Which RR source to use - 'rr_bbsig' (ECG derived) or 'rr_calc' (Polar derived)

    Returns:
    - rrs_dict (dict): Dictionary with 'RRI' (RR intervals in ms) and 'RRI_Time' (peak timestamps in ms).
      NeuroKit2 expects RR intervals in milliseconds for accurate HRV computation.
    """

    if not rr_dict or len(rr_dict) == 0:
        return {'RRI': np.array([]), 'RRI_Time': np.array([])}

    # Extract RR intervals from the specified source
    rr_intervals = np.array(rr_dict.get(rr_source, []))

    if len(rr_intervals) == 0:
        return {'RRI': np.array([]), 'RRI_Time': np.array([])}

    # Convert RR intervals from seconds (standard LSL/Unity unit) to milliseconds (standard HRV unit)
    rr_ms = rr_intervals * 1000

    # Calculate peak timestamps as cumulative sum of RR intervals
    # This reconstructs the relative timeline of heartbeats starting from 0
    timestamps_ms = np.cumsum(rr_ms)

    # Store in NeuroKit2-compatible format
    rrs_dict = {
        'RRI': rr_ms,
        'RRI_Time': timestamps_ms
    }

    return rrs_dict


def load_segmented_data(segmented_data_path):
    """ Load segmented data from gzip-compressed JSON file.

    Parameters:
    - segmented_data_path (Path): Path to the segmented_data.json file (gzip-compressed)

    Returns:
    - segmented_data (dict): Dictionary with 'baseline', 'approach', 'recovery', 'trials', and 'blocks' keys
    """

    if not segmented_data_path.exists():
        print(f"  Warning: Segmented data file not found: {segmented_data_path}")
        return {'baseline': [], 'approach': [], 'recovery': [], 'trials': [], 'blocks': []}

    with gzip.open(segmented_data_path, 'rt', encoding='utf-8') as f:
        segmented_data = json.load(f)

    return segmented_data


def hrv_time_domain(rrs_dict, subj='', segment_type='', segment_id=''):
    """
    Compute time-domain HRV metrics from RR intervals.
    Uses NeuroKit2's `hrv_time` function to calculate standard metrics.

    Parameters:
    - rrs_dict (dict): Dictionary with 'RRI' and 'RRI_Time'
    - subj (str): Subject ID
    - segment_type (str): Type of segment ('window', 'trial', or 'block')
    - segment_id (str): Identifier for the segment

    Returns:
    - hrv_time_metrics (dict): Dictionary with computed HRV metrics, or None if not enough data.
      Metrics include: MeanRR, MeanBPM, SDNN, RMSSD, pNN50.
    """

    # Check if we have valid RR data (need at least 2 beats for difference metrics like RMSSD)
    if len(rrs_dict.get('RRI', [])) < 2:
        return None

    try:
        # Calculate Time-domain HRV metrics using NeuroKit2
        # library automatically handles outlier checking if configured, but here we pass raw RRs
        hrv_t = nk.hrv_time(rrs_dict, show=show_plots)

        # Extract and format metrics
        # We handle potential None/NaN values from NeuroKit to ensure JSON serialization safety
        hrv_time_metrics = {
            'subject': subj,
            'segment_type': segment_type,
            'segment_id': segment_id,
            'MeanRR': float(hrv_t['HRV_MeanNN'][0]) if hrv_t['HRV_MeanNN'][0] is not None else None,
            'MeanBPM': float(60000 / hrv_t['HRV_MeanNN'][0]) if hrv_t['HRV_MeanNN'][0] is not None else None,
            'MedianRR': float(hrv_t['HRV_MedianNN'][0]) if hrv_t['HRV_MedianNN'][0] is not None else None,
            'MedianBPM': float(60000 / hrv_t['HRV_MedianNN'][0]) if hrv_t['HRV_MedianNN'][0] is not None else None,
            'MinRR': float(hrv_t['HRV_MinNN'][0]) if hrv_t['HRV_MinNN'][0] is not None else None,
            'MinBPM': float(60000 / hrv_t['HRV_MaxNN'][0]) if hrv_t['HRV_MaxNN'][0] is not None else None,
            'MaxRR': float(hrv_t['HRV_MaxNN'][0]) if hrv_t['HRV_MaxNN'][0] is not None else None,
            'MaxBPM': float(60000 / hrv_t['HRV_MinNN'][0]) if hrv_t['HRV_MinNN'][0] is not None else None,
            'SDNN': float(hrv_t['HRV_SDNN'][0]) if hrv_t['HRV_SDNN'][0] is not None else None,
            'SDSD': float(hrv_t['HRV_SDSD'][0]) if hrv_t['HRV_SDSD'][0] is not None else None,
            'RMSSD': float(hrv_t['HRV_RMSSD'][0]) if hrv_t['HRV_RMSSD'][0] is not None else None,
            'pnn50': float(hrv_t['HRV_pNN50'][0]) if hrv_t['HRV_pNN50'][0] is not None else None
        }
        return hrv_time_metrics
    except Exception as e:
        print(f"  Error computing time-domain HRV metrics: {e}")
        return None


def hrv_frequency_domain(rrs_dict, subj='', segment_type='', segment_id='', interpolation_rate=4):
    """ Compute frequency-domain HRV metrics from RR intervals.

    Parameters:
    - rrs_dict (dict): Dictionary with 'RRI' and 'RRI_Time'
    - subj (str): Subject ID
    - segment_type (str): Type of segment ('window', 'trial', or 'block')
    - segment_id (str): Identifier for the segment
    - interpolation_rate (float): Sampling frequency for interpolating RR intervals

    Returns:
    - hrv_freq_metrics (dict): Dictionary with computed HRV metrics, or None if not enough data
    """

    # Check if we have valid RR data
    if len(rrs_dict.get('RRI', [])) < 2:
        return None

    try:
        # Calculate Frequency-domain HRV metrics using NeuroKit2
        hrv_f = nk.hrv_frequency(rrs_dict, interpolation_rate=interpolation_rate,
                                 normalize=False, psd_method='welch', show=show_plots)

        # Extract and format metrics
        hrv_freq_metrics = {
            'subject': subj,
            'segment_type': segment_type,
            'segment_id': segment_id,
            'VLF': float(hrv_f['HRV_VLF'][0]) if hrv_f['HRV_VLF'][0] is not None else None,
            'LF': float(hrv_f['HRV_LF'][0]) if hrv_f['HRV_LF'][0] is not None else None,
            'HF': float(hrv_f['HRV_HF'][0]) if hrv_f['HRV_HF'][0] is not None else None,
            'LF/HF': float(hrv_f['HRV_LFHF'][0]) if hrv_f['HRV_LFHF'][0] is not None else None,
            'LFn': float(hrv_f['HRV_LFn'][0]) if hrv_f['HRV_LFn'][0] is not None else None,
            'HFn': float(hrv_f['HRV_HFn'][0]) if hrv_f['HRV_HFn'][0] is not None else None,
            'LnHF': float(hrv_f['HRV_LnHF'][0]) if hrv_f['HRV_LnHF'][0] is not None else None
        }
        return hrv_freq_metrics
    except Exception as e:
        print(f"  Error computing frequency-domain HRV metrics: {e}")
        return None

def save_hrv_results(hrv_results, output_dir, subject, session, run, segment_type, rr_source=''):
    """ Save HRV results for a specific segment type to JSON file.

    Parameters:
    - hrv_results (list): List of HRV metric dictionaries
    - output_dir (Path): Output directory
    - subject (str): Subject ID
    - session (str): Session ID
    - run (str): Run identifier
    - segment_type (str): Type of segment ('windows', 'trials', or 'blocks')
    - rr_source (str): RR source identifier ('bbsig' or 'calc')
    """

    if not hrv_results:
        return

    output_dir.mkdir(parents=True, exist_ok=True)

    # Add RR source to filename if specified
    if rr_source:
        output_file = output_dir / f"{subject}_{session}_task-TIDAL_{run}_hrv-{segment_type}_{rr_source}.json"
    else:
        output_file = output_dir / f"{subject}_{session}_task-TIDAL_{run}_hrv-{segment_type}.json"

    with open(output_file, 'w') as f:
        json.dump(hrv_results, f, indent=4)

    return output_file


