# Loop through subjects, sessions, and runs
for subject in subjects:
    for session in sessions:
        # Find all available runs for this subject/session
        subject_dir = segmenting_dir / subject / session

        if not subject_dir.exists():
            continue

        # Get all segmented data files for this subject/session
        segmented_files = sorted(subject_dir.glob(f"{subject}_{session}_task-TIDAL_run-*_segmented_data.json"))

        if not segmented_files:
            continue

        print(f"\n{subject} {session}:")

        for segmented_file in segmented_files:
            # Extract run number from filename
            run = segmented_file.name.split('task-TIDAL_')[1].split('_segmented')[0]

            # Load segmented data
            segmented_data = load_segmented_data(segmented_file)

            # Initialize results for each segment type and RR source
            trials_hrv_time_bbsig = []
            trials_hrv_freq_bbsig = []
            trials_hrv_time_calc = []
            trials_hrv_freq_calc = []
            blocks_hrv_time_bbsig = []
            blocks_hrv_freq_bbsig = []
            blocks_hrv_time_calc = []
            blocks_hrv_freq_calc = []

            # ========== PROCESS TRIALS ==========
            if analyze_trials and 'trials' in segmented_data:
                print(f"  Processing trials for {run}...")
                for trial_idx, trial in enumerate(segmented_data['trials']):
                    # Process both RR sources
                    for rr_source in ['rr_bbsig', 'rr_calc']:
                        rr_label = 'bbsig' if rr_source == 'rr_bbsig' else 'calc'

                        # Convert RR intervals to HRV format
                        rrs_dict = convert_peaks_to_rr(trial.get('ecg_peaks', {}), rr_source=rr_source)

                        # Compute HRV metrics if we have valid data
                        if len(rrs_dict['RRI']) > 0:
                            # Time-domain HRV
                            if compute_hrv_time:
                                trial_hrv_time = hrv_time_domain(
                                    rrs_dict,
                                    subj=subject,
                                    segment_type='trial',
                                    segment_id=trial.get('trial', f"trial_{trial_idx}")
                                )
                                if trial_hrv_time is not None:
                                    # Add timing and source information
                                    trial_hrv_time['trial_start'] = trial.get('trial_start')
                                    trial_hrv_time['trial_end'] = trial.get('trial_end')
                                    trial_hrv_time['trial'] = trial.get('trial')
                                    trial_hrv_time['run'] = run
                                    trial_hrv_time['session'] = session
                                    trial_hrv_time['rr_source'] = rr_source
                                    if rr_label == 'bbsig':
                                        trials_hrv_time_bbsig.append(trial_hrv_time)
                                    else:
                                        trials_hrv_time_calc.append(trial_hrv_time)

                            # Frequency-domain HRV
                            if compute_hrv_freq:
                                trial_hrv_freq = hrv_frequency_domain(
                                    rrs_dict,
                                    subj=subject,
                                    segment_type='trial',
                                    segment_id=trial.get('trial', f"trial_{trial_idx}"),
                                    interpolation_rate=interpolation_freq
                                )
                                if trial_hrv_freq is not None:
                                    # Add timing and source information
                                    trial_hrv_freq['trial_start'] = trial.get('trial_start')
                                    trial_hrv_freq['trial_end'] = trial.get('trial_end')
                                    trial_hrv_freq['trial'] = trial.get('trial')
                                    trial_hrv_freq['run'] = run
                                    trial_hrv_freq['session'] = session
                                    trial_hrv_freq['rr_source'] = rr_source
                                    if rr_label == 'bbsig':
                                        trials_hrv_freq_bbsig.append(trial_hrv_freq)
                                    else:
                                        trials_hrv_freq_calc.append(trial_hrv_freq)

                print(f"    bbsig: {len(trials_hrv_time_bbsig)} time-domain, {len(trials_hrv_freq_bbsig)} freq-domain")
                print(f"    calc:  {len(trials_hrv_time_calc)} time-domain, {len(trials_hrv_freq_calc)} freq-domain")

            # ========== PROCESS BLOCKS ==========
            if analyze_blocks and 'blocks' in segmented_data:
                print(f"  Processing blocks for {run}...")
                for block_idx, block in enumerate(segmented_data['blocks']):
                    # Process both RR sources
                    for rr_source in ['rr_bbsig', 'rr_calc']:
                        rr_label = 'bbsig' if rr_source == 'rr_bbsig' else 'calc'

                        # Convert RR intervals to HRV format
                        rrs_dict = convert_peaks_to_rr(block.get('ecg_peaks', {}), rr_source=rr_source)

                        # Compute HRV metrics if we have valid data
                        if len(rrs_dict['RRI']) > 0:
                            # Time-domain HRV
                            if compute_hrv_time:
                                block_hrv_time = hrv_time_domain(
                                    rrs_dict,
                                    subj=subject,
                                    segment_type='block',
                                    segment_id=block.get('block', f"block_{block_idx}")
                                )
                                if block_hrv_time is not None:
                                    # Add timing and source information
                                    block_hrv_time['block_start'] = block.get('block_start')
                                    block_hrv_time['block_end'] = block.get('block_end')
                                    block_hrv_time['block'] = block.get('block')
                                    block_hrv_time['run'] = run
                                    block_hrv_time['session'] = session
                                    block_hrv_time['rr_source'] = rr_source
                                    if rr_label == 'bbsig':
                                        blocks_hrv_time_bbsig.append(block_hrv_time)
                                    else:
                                        blocks_hrv_time_calc.append(block_hrv_time)

                            # Frequency-domain HRV
                            if compute_hrv_freq:
                                block_hrv_freq = hrv_frequency_domain(
                                    rrs_dict,
                                    subj=subject,
                                    segment_type='block',
                                    segment_id=block.get('block', f"block_{block_idx}"),
                                    interpolation_rate=interpolation_freq
                                )
                                if block_hrv_freq is not None:
                                    # Add timing and source information
                                    block_hrv_freq['block_start'] = block.get('block_start')
                                    block_hrv_freq['block_end'] = block.get('block_end')
                                    block_hrv_freq['block'] = block.get('block')
                                    block_hrv_freq['run'] = run
                                    block_hrv_freq['session'] = session
                                    block_hrv_freq['rr_source'] = rr_source
                                    if rr_label == 'bbsig':
                                        blocks_hrv_freq_bbsig.append(block_hrv_freq)
                                    else:
                                        blocks_hrv_freq_calc.append(block_hrv_freq)

                print(f"    bbsig: {len(blocks_hrv_time_bbsig)} time-domain, {len(blocks_hrv_freq_bbsig)} freq-domain")
                print(f"    calc:  {len(blocks_hrv_time_calc)} time-domain, {len(blocks_hrv_freq_calc)} freq-domain")

            # ========== SAVE RESULTS ==========
            output_dir = hrv_analysis_dir / subject / session

            # Save trials
            if analyze_trials:
                if compute_hrv_time and trials_hrv_time_bbsig:
                    file_path = save_hrv_results(trials_hrv_time_bbsig, output_dir, subject, session, run, 'trials_time', 'bbsig')
                    print(f"    Saved: {file_path.name}")
                if compute_hrv_time and trials_hrv_time_calc:
                    file_path = save_hrv_results(trials_hrv_time_calc, output_dir, subject, session, run, 'trials_time', 'calc')
                    print(f"    Saved: {file_path.name}")
                if compute_hrv_freq and trials_hrv_freq_bbsig:
                    file_path = save_hrv_results(trials_hrv_freq_bbsig, output_dir, subject, session, run, 'trials_freq', 'bbsig')
                    print(f"    Saved: {file_path.name}")
                if compute_hrv_freq and trials_hrv_freq_calc:
                    file_path = save_hrv_results(trials_hrv_freq_calc, output_dir, subject, session, run, 'trials_freq', 'calc')
                    print(f"    Saved: {file_path.name}")

            # Save blocks
            if analyze_blocks:
                if compute_hrv_time and blocks_hrv_time_bbsig:
                    file_path = save_hrv_results(blocks_hrv_time_bbsig, output_dir, subject, session, run, 'blocks_time', 'bbsig')
                    print(f"    Saved: {file_path.name}")
                if compute_hrv_time and blocks_hrv_time_calc:
                    file_path = save_hrv_results(blocks_hrv_time_calc, output_dir, subject, session, run, 'blocks_time', 'calc')
                    print(f"    Saved: {file_path.name}")
                if compute_hrv_freq and blocks_hrv_freq_bbsig:
                    file_path = save_hrv_results(blocks_hrv_freq_bbsig, output_dir, subject, session, run, 'blocks_freq', 'bbsig')
                    print(f"    Saved: {file_path.name}")
                if compute_hrv_freq and blocks_hrv_freq_calc:
                    file_path = save_hrv_results(blocks_hrv_freq_calc, output_dir, subject, session, run, 'blocks_freq', 'calc')
                    print(f"    Saved: {file_path.name}")

